void  HAL_UART_RxCpltCallback(UART_HandleTypeDef *huart)
{ 
	if(huart->Instance==USART1)
	{
		if(UART1_Rx_Buf[0]==0x80 && UART1_Rx_Buf[15]==0x7E)
		{
			if(UART1_Rx_Buf[1]==1)
			{
				get_motor_date_id=UART1_Rx_Buf[2];
				get_motor_date_flag=1;
			
			}
			else if(UART1_Rx_Buf[1]==2)
			{
				set_motor_id=UART1_Rx_Buf[2];	
				set_motor_flag=1;
			}
			else if(UART1_Rx_Buf[1]==3)
			{
				get_vofa_flag=1;
				get_vofa_id=UART1_Rx_Buf[2];
			}
		}
		HAL_UART_Receive_DMA(&huart1,UART1_Rx_Buf,16u);
	}
}
